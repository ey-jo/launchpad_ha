def get_closest_color(r: int, g: int, b: int) -> int:
    """
    Takes target R, G, B values and returns the palette index (0-127) 
    that is visually closest using 3D Euclidean distance.
    """
    closest_index = 0
    min_distance = float('inf')
    
    for index, (pr, pg, pb) in APPLE_II_128_PALETTE.items():
        # Standard 3D Euclidean distance formula
        distance = math.sqrt((r - pr) ** 2 + (g - pg) ** 2 + (b - pb) ** 2)
        
        if distance < min_distance:
            min_distance = distance
            closest_index = index
            
    return closest_index